// TestEmitFromSynth exercises Emit fed by Synth to verify end-to-end correctness.
func TestEmitFromSynth(t *testing.T) {
	surface := &metacli.ApiSurface{
		Assembly:        "CalcLib",
		Version:         "2.0.0",
		TargetFramework: "net8.0",
		Types: []metacli.SurfaceType{
			{
				FullName:  "CalcLib.Math",
				ShortName: "Math",
				Namespace: "CalcLib",
				Kind:      metacli.KindClass,
				IsStatic:  true,
				Methods: []metacli.SurfaceMethod{
					{
						Name:     "Add",
						IsStatic: true,
						ReturnType: metacli.TypeRef{
							FullName: "System.Int32",
							Kind:     metacli.TypeRefPrimitive,
						},
						Params: []metacli.ParamDef{
							{Name: "a", Type: metacli.TypeRef{FullName: "System.Int32", Kind: metacli.TypeRefPrimitive}},
							{Name: "b", Type: metacli.TypeRef{FullName: "System.Int32", Kind: metacli.TypeRefPrimitive}},
						},
					},
				},
			},
		},
	}
	s := shimgen.Synth("CalcLib", "2.0.0", "net8.0", surface)
	f := emit.Emit(s)

	if !strings.Contains(f.ExternMochi, "extern fun") {
		t.Errorf("ExternMochi missing extern fun\nfull:\n%s", f.ExternMochi)
	}
	if !strings.Contains(f.AliasMochi, "fun Math_Add") {
		t.Errorf("AliasMochi missing fun Math_Add\nfull:\n%s", f.AliasMochi)
	}
	if !strings.Contains(f.AliasMochi, "a: int, b: int") {
		t.Errorf("AliasMochi missing params 'a: int, b: int'\nfull:\n%s", f.AliasMochi)
	}
}
