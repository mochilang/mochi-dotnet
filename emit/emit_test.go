func TestEmitExternTypeDeclarations(t *testing.T) {
	s := jsonLikeShim()
	f := emit.Emit(s)
	if !strings.Contains(f.ExternMochi, "extern type JsonConvert") {
		t.Errorf("ExternMochi missing 'extern type JsonConvert'\nfull:\n%s", f.ExternMochi)
	}
	// extern type must precede extern fun.
	typeIdx := strings.Index(f.ExternMochi, "extern type JsonConvert")
	fnIdx := strings.Index(f.ExternMochi, "extern fun ")
	if typeIdx < 0 || fnIdx < 0 {
		t.Fatalf("missing extern type or extern fun in output")
	}
	if typeIdx > fnIdx {
		t.Errorf("extern type must precede extern fun (typeIdx=%d fnIdx=%d)", typeIdx, fnIdx)
	}
}

