func TestEmitVoidReturn(t *testing.T) {
	s := &shimgen.Shim{
		Package:        "Demo",
		PackageVersion: "0.1.0",
		Methods: []shimgen.ShimMethod{
			{
				EntryPoint:   "mochi_demo_util_reset",
				CSMethodName: "Util_Reset",
				UpstreamCall: "Demo.Util.Reset",
				Params:       nil,
				Return:       nil,
				IsStatic:     true,
			},
		},
	}
	f := emit.Emit(s)
	if !strings.Contains(f.ExternMochi, "extern fun mochi_demo_util_reset()\n") {
		t.Errorf("ExternMochi unit-return shape wrong:\n%s", f.ExternMochi)
	}
	if !strings.Contains(f.AliasMochi, "fun Util_Reset() {\n  mochi_demo_util_reset()\n}\n") {
		t.Errorf("AliasMochi unit-return body wrong:\n%s", f.AliasMochi)
	}
}

func TestEmitNoMethods(t *testing.T) {
	s := &shimgen.Shim{Package: "Empty", PackageVersion: "0.0.0"}
	f := emit.Emit(s)
	if !strings.Contains(f.ExternMochi, "// extern bindings for Empty 0.0.0") {
		t.Errorf("ExternMochi missing header for empty shim")
	}
	if strings.Contains(f.ExternMochi, "extern fun ") {
		t.Errorf("empty shim should have no extern fun lines")
	}
	if strings.Contains(f.ExternMochi, "extern type ") {
		t.Errorf("empty shim should have no extern type lines")
	}
}

func TestEmitDeterministic(t *testing.T) {
	s := jsonLikeShim()
	first := emit.Emit(s)
	for range 5 {
		got := emit.Emit(s)
		if got.ExternMochi != first.ExternMochi {
			t.Error("ExternMochi non-deterministic")
		}
		if got.AliasMochi != first.AliasMochi {
			t.Error("AliasMochi non-deterministic")
		}
	}
}

func TestEmitMochiRenderScalars(t *testing.T) {
	cases := []struct {
		mapping *typemap.Mapping
		want    string
	}{
		{&typemap.Mapping{Kind: typemap.KindBool}, "bool"},
		{&typemap.Mapping{Kind: typemap.KindByte}, "byte"},
		{&typemap.Mapping{Kind: typemap.KindInt}, "int"},
		{&typemap.Mapping{Kind: typemap.KindInt64}, "int64"},
		{&typemap.Mapping{Kind: typemap.KindUInt}, "uint"},
		{&typemap.Mapping{Kind: typemap.KindUInt64}, "uint64"},
		{&typemap.Mapping{Kind: typemap.KindFloat}, "float"},
		{&typemap.Mapping{Kind: typemap.KindFloat64}, "float64"},
		{&typemap.Mapping{Kind: typemap.KindChar}, "char"},
		{&typemap.Mapping{Kind: typemap.KindString}, "string"},
		{&typemap.Mapping{Kind: typemap.KindBytes}, "bytes"},
		{&typemap.Mapping{Kind: typemap.KindUnit}, "()"},
	}
	for _, c := range cases {
		got := emit.MochiRender(c.mapping)
		if got != c.want {
			t.Errorf("MochiRender(%v) = %q; want %q", c.mapping.Kind, got, c.want)
		}
	}
}

