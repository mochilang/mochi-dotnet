// jsonLikeSurface builds a shimgen.Shim that mirrors a minimal Newtonsoft.Json
// subset. We build the Shim directly (bypassing Synth) to control exact field
// values.
func jsonLikeShim() *shimgen.Shim {
	stringMapping := &typemap.Mapping{
		Kind:      typemap.KindString,
		CLRName:   "System.String",
		MochiType: "string",
	}
	handleMapping := &typemap.Mapping{
		Kind:       typemap.KindHandle,
		CLRName:    "Newtonsoft.Json.JsonConvert",
		MochiType:  "JsonConvert",
		HandleName: "JsonConvert",
	}
	intMapping := &typemap.Mapping{
		Kind:      typemap.KindInt,
		CLRName:   "System.Int32",
		MochiType: "int",
	}
	return &shimgen.Shim{
		Package:         "Newtonsoft.Json",
		PackageVersion:  "13.0.3",
		TargetFramework: "net8.0",
		Methods: []shimgen.ShimMethod{
			{
				EntryPoint:   "mochi_newtonsoftjson_jsonconvert_serializeobject",
				CSMethodName: "JsonConvert_SerializeObject",
				UpstreamCall: "Newtonsoft.Json.JsonConvert.SerializeObject",
				Params: []shimgen.ShimParam{
					{Name: "value", Mapping: *stringMapping},
				},
				Return:   stringMapping,
				IsStatic: true,
			},
			{
				EntryPoint:   "mochi_newtonsoftjson_jsonconvert_deserializeobject",
				CSMethodName: "JsonConvert_DeserializeObject",
				UpstreamCall: "Newtonsoft.Json.JsonConvert.DeserializeObject",
				Params: []shimgen.ShimParam{
					{Name: "json", Mapping: *stringMapping},
				},
				Return:   handleMapping,
				IsStatic: true,
			},
			{
				EntryPoint:   "mochi_newtonsoftjson_jsonconvert_getversion",
				CSMethodName: "JsonConvert_GetVersion",
				UpstreamCall: "Newtonsoft.Json.JsonConvert.GetVersion",
				Params:       nil,
				Return:       intMapping,
				IsStatic:     true,
			},
		},
	}
}

func TestEmitExternHeader(t *testing.T) {
	s := jsonLikeShim()
	f := emit.Emit(s)
	for _, want := range []string{
		"// extern bindings for Newtonsoft.Json 13.0.3",
		"// generated by github.com/mochilang/mochi-dotnet/emit; do not edit.",
	} {
		if !strings.Contains(f.ExternMochi, want) {
			t.Errorf("ExternMochi missing %q\nfull:\n%s", want, f.ExternMochi)
		}
	}
}

